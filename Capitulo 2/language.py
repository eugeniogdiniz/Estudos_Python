#Para acrescentar uma tabulação em seu texto, utilize a combinação de caracteres como mostrado em \t
print("Sempre\tEstudo")
#Para acrescentar uma quebra de linha em uma string, utilize a combinação de caracteres \n:
print("Sempre\tEstudo\nPython")
#Também podemos combinar tabulações e quebras de linha em uma única
#string. A string "\n\t" diz a Python para passar para uma nova linha e
#iniciar a próxima com uma tabulação.
print("Sempre\tEstudo\nPython\n\tAos Domingos")